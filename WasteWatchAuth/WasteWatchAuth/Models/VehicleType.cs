/// <summary>
/// Enumeration representing the types of vehicles used for waste collection.
/// </summary>
public enum VehicleType
{
	/// <summary>
	/// A front-loading vehicle, typically used for industrial or large-scale collections.
	/// </summary>
	FrontLoader,

	/// <summary>
	/// A compact vehicle designed for urban or narrow-street environments.
	/// </summary>
	UrbanMini,

	/// <summary>
	/// A side-loading vehicle, commonly used for residential areas.
	/// </summary>
	SideLoader,

	/// <summary>
	/// A rear-loading vehicle, often used in both residential and commercial collections.
	/// </summary>
	RearLoader
}